            <div class="home-institucion-contenedor-titulo">HISTORIA
            </div>
            <div class="home-institucion-contenedor-contenido" style="height: 94%"> 
                <div style="float: left">Verano de 1930. Con más expectativas que certezas, un ya lejano 29 de enero, llegaban a Balnearia las primeras religiosas de la Congregación de las Hermanas de
                    la Inmaculada de Génova.<br><br>Los comienzos no fueron sencillos. Las Hermanas sabían que habían llegado a un país junto a innumerables masas de inmigrantes europeos que, ante la
                    mecanización de las tareas rurales y el aumento de la población en el Viejo Continente, venían a buscar un porvenir mejor a estas tierras. Pero a ellas las traía un ideal superior,
                    el mismo que movió a San Agustín Roscelli a fundar una Congregación. Sin emargo, la adaptación al lugar y a sus costumbres fue dificultosa. Tanto que ese primer día en el pueblo, y
                    acabando de conocer el viejo hotel en desuso que sería su hogar, las religiosas se sentaron sobre sus baúles y lloraron.<br><br>
                </div>
                <img src="http://imageshack.com/a/img842/8171/hfbz.jpg" style="float: right; max-width: 35%">  
                <div>
                    Aún así, y con todas las entendibles incertidumbres y nostalgias
                    que las unían a sus comunidades de origen, a sus afectos y seguridades, no tardaron en abrazarse al carisma del Padre Fundador: con sencillez, humildad y entrega comenzaron las tareas
                    de limpieza y reacomodamiento del deterioro del edificio. La buena voluntad y acogida de los pobladores que no tardaron en darles bienvenida y en inscribir a sus hijas, las animó para
                    iniciar la actividad escolar.<br><br>  
                    Ese fue el origen. El Colegio recibió el nombre de Santa Teresita, patrona de las misiones, y fue confiado a ella el aliento y la fuerza para la superación en un camino poco llano.
                    Y la Santa no los desamparó. Día a día el pueblo crecía en afinidad y simpatía con la fundación del colegio y de la misma manera aumentaba el número de alumnas. Desde el comienzo
                    impartió la enseñanza primaria obligatoria, a la que se anexó el nivel inicial, las clases de labores y la academia de piano.<br><br>
                    A medida que las circunstancias lo exigieron, fue adoptándose a los nuevos requerimientos, hasta que las inundaciones de la zona 
                    exigieron que, con la ayuda de los padres, sea necesario rehacer el edificio, siguiendo las pautas de arquitectura escolar. A partir de entonces fue ampliándose sucesivamente 
                    hasta adquirir las dimensiones y comodidades actuales.<br><br>
            
                    En 1963 se crea una sección de varones hasta tercer grado, y se inicia el nivel secundario con la formación de 
                    Maestras Normales Nacionales, a la solicitud de los padres y la colaboración desinteresada de un grupo de docentes.<br>Dicho nivel fue modificando sus orientaciones según las 
                    necesidades del medio y las políticas vigentes<br><br>
                </div>
                <img src="http://imageshack.com/a/img824/2030/51dx.jpg" style="float: right; max-width: 35%">
                <div>
                    En mayo de 1964 se inauguraba el nuevo edificio del Instituto, fruto de tantas voluntades empeñadas en su logro.<br><br>En 
                    abril de 1976 se inició el Magisterio Superior, dando respuesta a la demanda de una sociedad que necesitaba tener maestros para sus escuelas, y siendo el primero de enseñanza privada
                    de la provincia de Córdoba, reconocido por su nivel de formación profesional. Los títulos otorgados eran Maestro Superior y Maestro Catequista.<br><br>
                
                
                    En 1987 se anexó al nivel inicial la sala de cuatro años y en 1996 la sala de tres años, que obtiene su reconocimiento pedagógico en el año 2001.<br><br>En 1994 ingresa el primer 
                    grupo de varones al nivel secundario, y se adecuaron los planes de estudio a la Ley Federal de Educación.<br><br>Se constituye la Cooperativa Escolar Juvenil Testimonio Ltda., primera 
                    Cooperativa de la Provincia en su tipo, matriculada provincialmente ante la Dirección de Fomento Cooperativo, e inscripta en el Registro Industrial de la Provincia. En el 2007 el 
                    Ministerio de Producción le otorga el Registro Nacional de Establecimiento (RNE).<br><br>En 1998 se 
                    iniciaron en el Nivel Terciario las gestiones para obtener el Profesorado de Nivel Inicial y la nueva aprobación para 
                    otorgar títulos de Profesor de Primero y Segundo Ciclo de E.G.B. <br><br>En la actualidad se está dictando el Profesorado de Biología, atendiendo a las demandas que surgen en el momento.
