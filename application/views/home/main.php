            <div class="home-institucion-contenedor-titulo">ESTILO DE FORMACIÓN Y ENSEÑANZA
            </div>
            <div class="home-institucion-contenedor-contenido"  style="height: 94%"> 
                <div style="float: left">El estilo de formación del Instituto Santa Teresita se caracteriza por “la caridad en el ocultamiento” que ha de animar a todos los miembros de la comunidad educativa 
                    para impulsarlos a realizar, a través de la educación, un servicio humilde y sencillo.<br><br>Por ello:<br>•   Busca formar, “no sólo expertos profesionales, sino sobre todo auténticos 
                    católicos” que miren, amen, imiten e invoquen a María, criatura nueva, y con su ayuda, vivan en amistad con Jesús, comprometidos en su Iglesia.<br>•   Asume la misión educadora como 
                    apostolado que exige el ejemplo y la presencia activa, en el ámbito de las actividades escolares, tanto de las Hermanas como de los docentes y de los padres.<br>•   Brinda asistencia 
                    espiritual y propicia la vida litúrgica y sacramental de todos los miembros de la comunidad educativa.<br>•   Procura que los alumnos, sintiéndose amados, busquen y amen la verdad y 
                    el conocimiento proporcionado por las ciencias humanas, en un clima de sencillez y serena alegría.<br>•   Promueve iniciativas que les proporcionen una formación completa y en las 
                    que puedan expresar la síntesis personal entre fe y cultura: encuentros, jornadas, retiros, grupos juveniles, visitas y ayuda a necesitados, campañas misioneras.<br>•   Emplea una 
                    metodología participativa que promueve el compromiso libre y responsable de cada miembro de la Comunidad Educativa.<br>•   Propicia una comunicación fluida y sincera entre directivos, 
                    docentes, exdocentes, padres, alumnos y exalumnos, para construir una comunidad educativa en la cual se formen personas capaces de interactuar con madurez y desempeñar su rol con dignidad.
                    <br>•   Estimula,  desde el Equipo de animación pastoral,  una labor que sea educativo-pastoral y que “proyecte todas las asignaturas a la luz de la fe y la recta moral”. (Dir. 170)<br>
                    •   Busca la forma de trabajar en la mayor cantidad de actividades posibles a través de conexiones interdisciplinarias, a fin de lograr resultados educativos más integrales y completos.
                    <br>•   Tiende a adecuar las estrategias de enseñanza a las necesidades de los alumnos creando un ambiente que favorezca la comunicación, la cooperación, el debate, el intercambio de 
                    experiencias, de reflexión, de modo que todos los alumnos desarrollen al máximo sus potencialidades, atendiendo a la diversidad individual y sociocultural.<br>•   Fomenta una cultura de 
                    formación permanente del equipo docente a través de la reflexión sobre la propia práctica.<br>•   Genera, a través del Equipo de Conducción, estrategias para consolidar el trabajo en 
                    equipo como herramienta de resolución de problemas, procurando vencer el individualismo y propiciando relaciones colaborativas; sin excluir formas de cultura mixtas, como respuesta a la 
                    diferenciación de niveles y turnos de funcionamiento.<br>•   Establece vínculos y genera acciones con las instituciones de la comunidad local y regional favoreciendo el desarrollo del 
                    bien común.<br><br>Esta animación, tanto en lo educativo como en lo catequístico-pastoral, es promovida por la superiora de la comunidad, la representante legal, los directivos y el 
                    capellán del Colegio. Colaboran directamente los jefes de Departamentos de materias afines, los alumnos delegados de cada curso y los padres representantes de cada curso.<br><br>Derivados 
