            <div class="home-institucion-contenedor-titulo">NATURALEZA
            </div>
            <div class="home-institucion-contenedor-contenido" style="height: 83%"> 
                <div style="float: left">El <b>Instituto Santa Teresita</b> es un centro educativo de la Iglesia Católica al servicio de la formación de la niñez, la adolescencia y la juventud. Está dirigido por 
                    las Hermanas de la Inmaculada de Génova, de acuerdo con las disposiciones de la Iglesia, de la propia Congregación y las normas educativas de la Nación Argentina y de la Provincia de 
                    Córdoba para los Institutos privados adscriptos a la Enseñanza Oficial.<br><br>Está ubicado en la zona urbana de la localidad de Balnearia, del departamento San Justo. Su zona de 
                    influencia es amplia. Se extiende a las zonas rurales y a las localidades vecinas de Miramar, Marull, Villa Concepción del Tío, La Para, Villa Fontana, La Puerta, Obispo Trejo, Altos 
                    de Chipión, La Paquita y Colonia Marina.<br><br>Imparte educación Inicial, Primaria, Secundaria  y Superior de Formación Docente, siguiendo el carisma de San Agustín Roscelli, quien 
                    movido por un profundo espíritu de pastor atento, supo leer y comprometerse con las realidades de su tiempo. Fundó en 1876 el Instituto de las Hermanas de la Inmaculada para que se 
                    dedicara especialmente a la educación de niños y jóvenes de toda condición, para “hacerles conocer y amar a Jesús, Hijo de Dios encarnado por nuestra salvación y disponerlos al 
                    encuentro con el Padre, cuya comunión los transformará en hombres nuevos”. (Const. 5)<br><br>El Instituto religioso dirige otros diez colegios en Argentina, uno en Chile, varios en 
                    Italia, dos en Canadá, uno en Rumania y uno en Etiopía, además de realizar otras obras de servicio y de caridad.
